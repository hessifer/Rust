#[allow(dead_code)]
pub fn numeric_operations() {
    // addition
    let sum = 5 + 5;

    // subtraction
    let difference = 10 - 5;

    // multiplication
    let product = 5 * 5;

    // division
    let quotient = 56.7 / 32.2;

    // remainder
    let remainder = 43 % 5;

    println!("Sum of 5 and 5: {}", sum);
    println!("Difference of 10 and 5: {}", difference);
    println!("Product of 5 and 5: {}", product);
    println!("Quotient of 56.7 divided by 32.2: {}", quotient);
    println!("Quotient Two Decimal Places: {:.2}", quotient);
    println!("Remainder of 43 divided by 5: {}", remainder);
}